package domain;

public class FilmVo {
	private int fidx;
	private String filmCategory;
	private String filmName;
	private String filmDate;
	private String filmStatus;
	private String filmDetail;
	private String filmPoster;
	private int midx;
	public int getFidx() {
		return fidx;
	}
	public void setFidx(int fidx) {
		this.fidx = fidx;
	}
	public String getFilmCategory() {
		return filmCategory;
	}
	public void setFilmCategory(String filmCategory) {
		this.filmCategory = filmCategory;
	}
	public String getFilmName() {
		return filmName;
	}
	public void setFilmName(String filmName) {
		this.filmName = filmName;
	}
	public String getFilmDate() {
		return filmDate;
	}
	public void setFilmDate(String filmDate) {
		this.filmDate = filmDate;
	}
	public String getFilmStatus() {
		return filmStatus;
	}
	public void setFilmStatus(String filmStatus) {
		this.filmStatus = filmStatus;
	}
	public String getFilmDetail() {
		return filmDetail;
	}
	public void setFilmDetail(String filmDetail) {
		this.filmDetail = filmDetail;
	}
	public String getFilmPoster() {
		return filmPoster;
	}
	public void setFilmPoster(String filmPoster) {
		this.filmPoster = filmPoster;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	
	
	
	
	
	
	
	
	
	
	
}
	